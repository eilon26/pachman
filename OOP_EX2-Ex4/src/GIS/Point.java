package GIS;

public class Point {
	int x;
	int y;
	
	public Point(int x,int y) {
		this.x=x;
		this.y=y;
	}

	public int getX() {
		return x;
	}

	public int getY() {
		return y;
	}
	
	public void setX(int num) {
		this.x = num;
	}
	
	public void setY(int num) {
		this.y = num;
	}
	
}
